	function get__yoil_kor_s($yoil_n)
	{

		//=====================================================
		// 시작 => 요일__구분
		SWITCH ($yoil_n)
		{

			CASE 1 :			$yoil_s = "월요일";		 $yoil_span_s = "<span style=\"color:#222222\">월요일</span>";		 BREAK;
			CASE 2 :			$yoil_s = "화요일";		 $yoil_span_s = "<span style=\"color:#222222\">화요일</span>";		 BREAK;
			CASE 3 :			$yoil_s = "수요일";		 $yoil_span_s = "<span style=\"color:#222222\">수요일</span>";		 BREAK;
			CASE 4 :			$yoil_s = "목요일";		 $yoil_span_s = "<span style=\"color:#222222\">목요일</span>";		 BREAK;
			CASE 5 :			$yoil_s = "금요일";		 $yoil_span_s = "<span style=\"color:#222222\">금요일</span>";		 BREAK;
			CASE 6 :			$yoil_s = "토요일";		 $yoil_span_s = "<span style=\"color:#0000ff\">토요일</span>";		 BREAK;
			DEFAULT :		 $yoil_s = "일요일";		 $yoil_span_s = "<span style=\"color:#ff0000\">일요일</span>";		 BREAK;

		}
		// 끝 => 요일__구분
		//=====================================================


		//=====================================================
		// 요일__배열
		$yoil_arr["yoil_s"] = $yoil_s;
		$yoil_arr["yoil_span_s"] = $yoil_span_s;


		//=====================================================
		// 넘겨주기
		return $yoil_arr;

	}
