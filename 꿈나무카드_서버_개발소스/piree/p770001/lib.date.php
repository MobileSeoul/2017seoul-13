	function get__year_month__before_next($year_n, $month_n)
	{

		//=====================================================
		// 시작 => 년도__없으면
		IF (!$year_n || $year_n < 1)
		{
			$year_n = date("Y");

		}
		// 끝 => 년도__없으면
		//=====================================================


		//=====================================================
		// 시작 => 월__없으면
		IF (!$month_n || $month_n < 1)
		{
			$month_n = date("m");

		}
		// 끝 => 월__없으면
		//=====================================================


		//=====================================================
		// 이전_년도
		$date_arr["befor_year_n"] = $year_n;


		//=====================================================
		// 이전_월
		$date_arr["befor_month_n"] = $month_n;


		//=====================================================
		// 다음_년도
		$date_arr["next_year_n"] = $year_n;


		//=====================================================
		// 다음_월
		$date_arr["next_month_n"] = $month_n;


		//=====================================================
		// 시작 => 월__구분
		SWITCH ($month_n)
		{

			#####################################################
			# 시작 => 12월__이면
			CASE 1 :

				// 이전_년도
				$date_arr["befor_year_n"] = $year_n-1;

				// 이전_월
				$date_arr["befor_month_n"] = 12;

				// 다음_월
				$date_arr["next_month_n"] = 2;

			BREAK;
			# 끝 => 1월_부터_11월__이면
			#####################################################


			#####################################################
			# 시작 => 12월__이면
			CASE 12 :

				// 다음_년도
				$date_arr["next_year_n"] = $year_n+1;

				// 이전_월
				$date_arr["befor_month_n"] = 11;

				// 다음_월
				$date_arr["next_month_n"] = 1;

			BREAK;
			# 끝 => 1월_부터_11월__이면
			#####################################################


			#####################################################
			# 시작 => 1월_11월__사이_이면
			DEFAULT :

				// 이전_월
				$date_arr["befor_month_n"] = $month_n-1;

				// 다음_월
				$date_arr["next_month_n"] = $month_n+1;

			BREAK;
			# 끝 => 1월_11월__사이_이면
			#####################################################

		}
		// 끝 => 월__구분
		//=====================================================


		//=====================================================
		// 넘겨주기
		return $date_arr;

	}
	#########################################################
	# 끝 => 날짜__이전_다음__년도_월__알아내기
	#########################################################



